<h3><a name="hours" id="hours"></a>Practice Hours</h3>
<p>8am - 5pm Monday - Friday<br />
As our practice grows, we plan to expand our office hours to include hours after 5pm and early morning appointments before 8am during the weekdays, and Saturday hours.</p>

<h3><a name="apptinfo" id="apptinfo"></a>Appointment Information</h3>
<p>Appointments can be anytime during normal business hours Monday through Friday.<br />
We will be closed for the following holidays:<br />
Memorial Day<br />
4th of July<br />
Labor Day<br />
Thanksgiving<br />
Christmas<br />
New Years Day<br />
</p>

<h3><a name="afterhours" id="afterhours"></a>After Hours</h3>
<p>You will be directed to our answering service after hours.  Our on-call physicians welcome your calls after hours as we consider it a privilege to serve you and your family not just during business hours.  Inpatient (hospitalization) services are provided by inpatient specialist groups at Onslow Memorial Hospital.</p>

<h3><a name="newpatientinfo" id="newpatientinfo"></a>New Patient Info</h3>
<p>Plan to arrive at least 20 minutes prior to your first appointment to ensure a smooth transition to our practice. Please bring to your appointment the following:<br />
<!---FORMS--- Please fill out and bring any appropriate <a href="forms.php">forms from our website</a><br />-->
- All of your medications that you take on a regular basis<br />
- Your insurance card and a picture ID<br />
- Medical records from any prior physicians that you think may be helpful. We will be happy to obtain any medical records as well with your permission.<br /><br />

All patients will have their pictures taken at their first visit for identification purposes. This is to protect you and your family as well as us.</p>

<h3><a name="followup" id="followup"></a>Follow-up Visits</h3>
<p>Please arrive 10 minutes prior to appointment. Please bring to any visit all of your medications, or a detailed list of your medications containing strengths and dosages, to ensure we care for you in the safest manner possible. Please bring your insurance card to every visit as well.  This ensures that you receive all of your insurance benefits by allowing us to have your correct insurance information in our system when we submit claims.</p>

<h3><a name="walkins" id="walkins"></a>Walk-In and Same Day Appointment Information</h3>
<p>We welcome same day appointments!  We strive to see new and established patients within 24 hours, regardless of the reason and for issues ranging from acute illnesses to physical examinations.</p>

<p>Walk-in visits are not encouraged.  However, if it is urgent and you are not able to reach us prior to your arrival, we will attempt to work you in as quickly as possible, realizing that you may have a prolonged wait as we care for previously scheduled patients. </p>

<h3><a name="cancellations" id="cancellations"></a>Late Appointment and Cancellation Policy</h3>
<p>If an appointment must be canceled, then please notify us as soon as possible.  You will not be charged any fees for cancelled visits, no matter how soon a notice we receive.  Informing us of your cancellation simply allows us to see other patients that may need to be worked in and opens up other scheduling options for patients.</p>
<p>If your are running late to the appointment, then please let us know so we can adjust our schedule accordingly.</p>




<h3><a name="cellphonepolicy" id="cellphonepolicy"></a>Cell Phone Policy</h3>
<p>Please do your best to silence your cell phones upon entering our practice to better serve you and our patients.</p>

<h3><a name="refill1" id="refill1"></a>Prescription Refill Requests</h3>
<p>The best time to obtain prescription refills is during your appointment. Otherwise, the best way to obtain refill requests is to call your pharmacy and they will contact us with all the pertinent information we need.  We will do our very best to refill your prescriptions as soon as possible and most prescriptions are refilled within 24 hours.  Like most physician offices, there are times in which we will request an office visit before refilling a prescription.  However, we will gladly supply you a prescription for enough of your medication to last you until we can see you in the office. The on-call physician will not refill medications after regular office hours.</p>

<h3><a name="refill2" id="refill2"></a>Refill of Controlled Drugs</h3>
<p>Refill of controlled drug prescriptions (narcotics, anxiety medications, sedatives) will be limited to those originated at our practice until we can review copies of your prior physician's records. Formal, written, and signed medication plans may be required for ongoing controlled drug use.  For more complex and prolonged cases we will refer to local specialists (pain medicine physicians, psychiatrists, etc).</p>

<h3><a name="insurance" id="insurance"></a>Insurance Policies Accepted</h3>
<p>Always check with your insurance company before selecting a physician.<br />
We accept the following insurance plans:<br />
most private insurance (e.g. Blue Cross Blue Shield, United Health Care, Medcost)<br />
Medicare<br />
Medicaid<br />
Tricare Prime and Tricare Standard<br />
</p>

<h3><a name="referrals" id="referrals"></a>Referrals</h3>
<p>Should it become necessary for us to refer you to a specialist, or order a procedure or diagnostic test as part of your care, we will contact your insurance company, verify the requirements of your plan and obtain the necessary authorizations for your referral. Please allow 48 hours from the time you see us to process the proper authorization.  After the proper authorizations are secured from the insurance company, we will happily assist you with scheduling your appointment. We cannot obtain &quot;retro&quot; authorizations for coverage unless the care provided was the result of hospitalization or the need was emergent. Some insurance plans and specialists do not require primary care involvement to schedule an appointment.</p>

<h3><a name="labtests" id="labtests"></a>Laboratory Tests and Results</h3>
<p>We will contact you immediately with any critical lab results. For routine lab tests, please allow five working days for the results.  Depending on the physician order, you will either be notified by phone, letter, or at your next office visit.  If you do not hear from us within two weeks, then please call us during normal business hours.</p>

<h3><a name="billing" id="billing"></a>Billing</h3>
<p>All insurance co-payments and deductibles are due at the time of service.</p>

<h3><a name="release" id="release"></a>Medical Records Release</h3>
<p>To release records to a specialist or another practice, we need your written permission and we have forms in our office for that purpose. The process of transferring records is time consuming, and we ask for two weeks to complete the transfer. <!--A records release form is available for download on our <a href="forms.php">Forms Page</a> -->
</p>

<h3><a name="privacy" id="privacy"></a>Privacy Policy and Patients Rights </h3>
<p>As a patient, you are a partner in your care. Consequently you have rights and responsibilities. <a href="forms/OPC-Patient-Rights-and-Responsibilities.pdf" target="_blank">Click here</a> to download printable versions of our Patient Rights and Responsibilities form.</p><p>You can also download our Privacy Policy by <a href="forms/Privacy-Policy.pdf" target="_blank">clicking here</a>.</p>





