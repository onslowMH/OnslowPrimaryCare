    <h1 class="alt">SERVICES</h1>
<p>We manage acute and chronic medical problems throughout all the stages of your life. Complex or unusual problems may require the involvement of specialists and we take pride in facilitating this as we orchestrate your overall care.</p>

<p>The following is a sample of the services that we provide in addition to managing chronic medical conditions for those 14 years of age and up.</p>

<ul class="servicesUL">
<li>Physical Examinations for all occasions, including routine physicals, pre-participation sports physicals for those 14 years of age and up, school and college physicals and pre-employment physicals.</li>

<li>Women's Health Services including: Preventative Well Women Exams (full physicals), pelvic exams, breast exams, STD screening, mammogram referral/orders, weight loss, hormonal imbalance (thyroid/menopause), urinary tract infection (UTI), pregnancy tests, birth control, irregular periods.</li>

<li>Management of acute illnesses (e.g. strep throat, ear infections, bronchitis/pneumonia, sinus infections, urinary tract infections).</li>

<li>Management of acute injuries (e.g. sports injuries and suspected broken bones, stitches/sutures for wounds).</li>

<li>On site diagnostic services including laboratory testing for acute and chronic illnesses (e.g. rapid strep test,  labs to monitor proper medication dosages)</li>

<li>Procedures (e.g. EKG, pulmonary function testing, ingrown toenail removal, joint injections)</li>

</ul>

<h3>Some examples of chronic illnesses that we manage in our office include:</h3>
<ul class="servicesUL">
<li>Cardiovascular disorders (e.g. high blood pressure, coronary artery disease, heart failure, high cholesterol)</li>
<li>Pulmonary disorders (e.g. emphysema/COPD, asthma)</li>
<li>Gastrointestinal disorders (e.g. esophageal reflux/GERD, diverticulosis, etc)</li>
<li>Neurological disorders (e.g. dementia, headaches)</li>
<li>Dermatologic and Allergic disorders (e.g. eczema, acne, seasonal allergies)</li>
<li>Genitourinary disorders (e.g. genital infections, enlarged prostate, erectile dysfunction)</li>
<li>Endocrine disorders (e.g. insulin and non-insulin dependent diabetes, thyroid conditions, osteoporosis, obesity)</li>
<li>Musculoskeletal disorders (e.g. sports injuries, arthritis, joint/back pain)</li>
<li>Behavioral health problems (e.g. anxiety, depression, ADHD/ADD, smoking cessation)</li>
<li>Hematological disorders (e.g. anemia, Coumadin management) </li>
</ul>



